La class Recon est un vaisseaux (voir class Ship).
La class Recon possede egalement une arme (voir interface IWeapon).
De ce fait, la class possede tous les attributs de Ship et methode de IWeapon.
Une constante PP est presente pour reset le nombre de PP propre a la class recon.
La methode fire (obligatoire  because IWeapon), tire et inflige des degats sur,
une ligne ou un colone en fonction de la direction de Recon (voir $dir dans Ship.doc.txt).
